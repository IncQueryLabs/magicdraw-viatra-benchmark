package com.incquerylabs.magicdraw.benchmark;

public class InvalidBenchmarkParameterizationException extends RuntimeException {

	private static final long serialVersionUID = -9034545697043043723L;

	public InvalidBenchmarkParameterizationException() {
		super();
	}

	public InvalidBenchmarkParameterizationException(String message, Throwable cause, boolean enableSuppression,
			boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);
	}

	public InvalidBenchmarkParameterizationException(String message, Throwable cause) {
		super(message, cause);
	}

	public InvalidBenchmarkParameterizationException(String message) {
		super(message);
	}

	public InvalidBenchmarkParameterizationException(Throwable cause) {
		super(cause);
	}

	
}
